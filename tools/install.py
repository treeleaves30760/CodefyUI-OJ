# Wizard steps
# ---------------------------------------------------------------------------


def step_env_check(ports: list[int], non_interactive: bool = False) -> bool:
    console.rule("[bold cyan]Step 1 / Environment check[/bold cyan]")
    results = env_checks(ports)
    console.print(render_check_table(results))

    fatal = [r for r in results if not r[1] and not r[0].startswith("Port")]
    port_warn = [r for r in results if not r[1] and r[0].startswith("Port")]
    if fatal:
        console.print(
            "\n[red]One or more required tools are missing.[/red]\n"
            "Install Docker + the compose plugin and re-run.",
        )
        return False
    if port_warn:
        names = ", ".join(r[0] for r in port_warn)
        console.print(
            f"\n[yellow]Heads up: {names}. The stack will fail to start if these "
            "stay occupied. You can pick a different frontend port in the next step; "
            "DB/Redis/API ports are not configurable here.[/yellow]",
        )
        if non_interactive:
            return True
        if not questionary.confirm("Continue anyway?", default=False).ask():
            return False
    return True


def step_mode(current: Settings) -> str:
    console.rule("[bold cyan]Step 2 / Deployment mode[/bold cyan]")
    return questionary.select(
        "Which install mode?",
        choices=[
            Choice(
                title="Competition — full auth, admin setup, contests, leaderboard",
                value="competition",
                description="Multi-user. Students register / sign in. Admin manages problems and contests.",
            ),
            Choice(
                title="Practice — no login, shared local user, no contests",
                value="practice",
                description="One-machine local use. Skip admin/login entirely.",
            ),
        ],
        default=current.mode,
    ).ask() or current.mode


def step_admin(s: Settings) -> None:
    if s.mode != "competition":
        s.bootstrap_admin = False
        return

    console.rule("[bold cyan]Step 3 / Admin account[/bold cyan]")
    console.print(
        "You have two options for creating the first administrator:\n"
        "  • Pre-create here via env vars (auto-created on first boot)\n"
        "  • Skip and use the [bold]/setup[/bold] UI on first visit to localhost",
    )
    s.bootstrap_admin = questionary.confirm(
        "Pre-create admin now?", default=s.bootstrap_admin
    ).ask() or False

    if not s.bootstrap_admin:
        s.admin_email = ""
        s.admin_password = ""
        return

    s.admin_display_name = questionary.text(
        "Admin display name:",
        default=s.admin_display_name or "Admin",
    ).ask() or "Admin"
    s.admin_email = questionary.text(
        "Admin email:",
        default=s.admin_email,
        validate=lambda v: "@" in v and "." in v.split("@", 1)[1] or "Enter a valid email",
    ).ask() or ""
    s.admin_password = questionary.password(
        "Admin password (≥ 8 chars):",
        validate=lambda v: len(v) >= 8 or "At least 8 characters",
    ).ask() or ""


def step_other(s: Settings) -> None:
    console.rule("[bold cyan]Step 4 / Other settings[/bold cyan]")

    try:
        s.frontend_port = int(
            questionary.text(
                "Frontend port:",
                default=str(s.frontend_port),
                validate=lambda v: v.isdigit() and 1 <= int(v) <= 65535 or "1-65535",
            ).ask()
            or str(s.frontend_port)
        )
    except ValueError:
        pass
    s.cors_origins = s.default_cors_origins()

    use_existing_db = questionary.confirm(
        f"Use generated Postgres password (starts {s.db_password[:6]}…)?",
        default=True,
    ).ask()
    if use_existing_db is False:
        s.db_password = questionary.password("Postgres password:").ask() or s.db_password

    use_existing_jwt = questionary.confirm(
        f"Use generated JWT secret (starts {s.jwt_secret[:6]}…)?",
        default=True,
    ).ask()
    if use_existing_jwt is False:
        s.jwt_secret = questionary.text("JWT secret:").ask() or s.jwt_secret


def _mask(value: str, keep: int = 4) -> str:
    if not value:
        return ""
    return value[:keep] + "•" * 8


def render_preview(s: Settings) -> Table:
    t = Table(show_header=False, box=None, pad_edge=False)
    t.add_column(style="bold")
    t.add_column()
    t.add_row("Mode", s.mode)
    t.add_row("Frontend URL", f"http://localhost:{s.frontend_port}")
    t.add_row("API", f"http://localhost:{DEFAULT_API_PORT}")
    t.add_row("Postgres password", _mask(s.db_password))
    t.add_row("JWT secret", _mask(s.jwt_secret))
    if s.mode == "competition":
        if s.bootstrap_admin:
            t.add_row("Admin", f"{s.admin_email} ({s.admin_display_name})")
            t.add_row("Admin password", _mask(s.admin_password))
        else:
            t.add_row("Admin", "[dim]created via /setup UI on first visit[/dim]")
    else:
        t.add_row("Admin", "[dim]n/a (practice mode)[/dim]")
    return t


def step_preview_and_confirm(s: Settings) -> str:
    """Returns 'install' | 'back' | 'abort'."""
    console.rule("[bold cyan]Step 5 / Preview[/bold cyan]")
    console.print(render_preview(s))
    console.print("\n[dim]Full .env will be written to:[/dim] " + str(ENV_PATH))
    return questionary.select(
        "Proceed?",
        choices=[
            Choice("Yes — write .env and run docker compose up -d --build", value="install"),
            Choice("Back — change a setting", value="back"),
            Choice("Abort — exit without writing", value="abort"),
        ],
    ).ask() or "abort"


# ---------------------------------------------------------------------------
# Install / docker compose orchestration
# ---------------------------------------------------------------------------


def write_env(s: Settings) -> None:
    if ENV_PATH.exists():
        backup = ENV_PATH.with_suffix(ENV_PATH.suffix + ".bak")
        ENV_PATH.replace(backup)
        console.print(f"[dim]Existing .env moved to {backup.name}[/dim]")
    ENV_PATH.write_text(s.render_env(), encoding="utf-8")
    console.print(f"[green]Wrote {ENV_PATH}[/green]")


def docker_compose_up() -> int:
    cmd = ["docker", "compose", "-f", str(COMPOSE_FILE), "up", "-d", "--build"]
    console.print(f"\n[dim]$ {' '.join(cmd)}[/dim]\n")
    proc = subprocess.run(cmd, cwd=str(REPO_ROOT))
    return proc.returncode


def wait_for_health(timeout_sec: int = 120) -> bool:
    import urllib.error
    import urllib.request

    url = f"http://localhost:{DEFAULT_API_PORT}/api/health"
    deadline = time.monotonic() + timeout_sec
    last_err = ""
    with console.status(f"Waiting for {url} to respond …", spinner="dots"):
        while time.monotonic() < deadline:
            try:
                with urllib.request.urlopen(url, timeout=3) as resp:
                    if resp.status == 200:
                        return True
            except (urllib.error.URLError, ConnectionError, TimeoutError) as e:
                last_err = str(e)
            time.sleep(2)
    console.print(f"[red]API did not become healthy within {timeout_sec}s: {last_err}[/red]")
    return False


def show_success(s: Settings) -> None:
    console.rule("[bold green]Install complete[/bold green]")
    front = f"http://localhost:{s.frontend_port}"
    console.print(Panel.fit(
        (
            f"Frontend:  [bold]{front}[/bold]\n"
            f"API:       http://localhost:{DEFAULT_API_PORT}/api/health\n\n"
            + (
                f"[bold]Next:[/bold] open the frontend and create the admin "
                f"via the [bold]/setup[/bold] screen.\n"
                "When done, you can remove the BOOTSTRAP_ADMIN_* lines from .env."
                if s.mode == "competition" and not s.bootstrap_admin
                else (
                    f"[bold]Admin:[/bold] {s.admin_email} (the password you entered).\n"
                    "Remove BOOTSTRAP_ADMIN_* from .env after first successful boot."
                    if s.mode == "competition"
                    else "[bold]Practice mode:[/bold] no login required. "
                         "Open the frontend and start solving."
                )
            )
        ),
        border_style="green",
    ))
    console.print(
        "\nUseful commands:\n"
        "  docker compose logs -f api      # tail API logs\n"
        "  docker compose down             # stop (keep volumes)\n"
        "  docker compose down -v          # stop + wipe DB\n"
    )


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def main() -> int:
    parser = argparse.ArgumentParser(description="CodefyUI-OJ install wizard")
    parser.add_argument(
        "--check", action="store_true", help="Run environment checks and exit",
    )
    parser.add_argument(
        "--no-up",
        action="store_true",
        help="Write .env but don't run docker compose up at the end",
    )
    args = parser.parse_args()

    console.print(Panel.fit(
        "[bold cyan]CodefyUI-OJ installer[/bold cyan]\n"
        "Interactive setup for the docker compose stack.",
        border_style="cyan",
    ))

    settings = settings_from_existing(parse_existing_env(ENV_PATH))
    if not settings.cors_origins:
        settings.cors_origins = settings.default_cors_origins()

    ports_to_check = [
        settings.frontend_port,
        DEFAULT_API_PORT,
        DEFAULT_DB_PORT,
        DEFAULT_REDIS_PORT,
    ]
    if args.check:
        ok = step_env_check(ports_to_check, non_interactive=True)
        return 0 if ok else 1

    if not step_env_check(ports_to_check):
        return 1

    while True:
        settings.mode = step_mode(settings)
        step_admin(settings)
        step_other(settings)
        # CORS depends on frontend port — recompute after step_other if changed.
        settings.cors_origins = settings.default_cors_origins()

        decision = step_preview_and_confirm(settings)
        if decision == "install":
            break
        if decision == "abort":
            console.print("Aborted. No files written.")
            return 0
        # back: loop

    write_env(settings)
    if args.no_up:
        console.print("[yellow]--no-up: skipping docker compose up.[/yellow]")
        return 0

    rc = docker_compose_up()
    if rc != 0:
        console.print(f"[red]docker compose exited with code {rc}.[/red]")
        return rc

    if not wait_for_health():
        console.print(
            "Stack started but the API isn't responding yet. Check logs with:\n"
            "  docker compose logs -f api",
        )
        return 1

    show_success(settings)
    return 0


if __name__ == "__main__":
    sys.exit(main())
