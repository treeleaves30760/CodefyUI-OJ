"""CodefyUI-OJ installer wizard.

Runs an interactive setup: environment check → mode (competition/practice) →
config (admin, JWT secret, DB password, ports) → preview .env → write + docker
compose up -d --build. Idempotent: re-running picks up your existing .env as
defaults.

Usage:
    uv run tools/install.py        # full wizard
    uv run tools/install.py --check  # env check only, no prompts
"""
from __future__ import annotations

import argparse
import os
import re
import secrets
import shutil
import socket
import subprocess
import sys
import time
from dataclasses import dataclass, field
from pathlib import Path

import questionary
from questionary import Choice
from rich.console import Console
from rich.panel import Panel
from rich.table import Table

REPO_ROOT = Path(__file__).resolve().parent.parent
ENV_PATH = REPO_ROOT / ".env"
COMPOSE_FILE = REPO_ROOT / "docker-compose.yml"

DEFAULT_FRONTEND_PORT = 8080
DEFAULT_API_PORT = 8100
DEFAULT_DB_PORT = 5432
DEFAULT_REDIS_PORT = 6379

console = Console()


@dataclass
class Settings:
    mode: str = "competition"
    bootstrap_admin: bool = False
    admin_email: str = ""
    admin_password: str = ""
    admin_display_name: str = "Admin"
    db_password: str = field(default_factory=lambda: secrets.token_urlsafe(24))
    jwt_secret: str = field(default_factory=lambda: secrets.token_urlsafe(48))
    frontend_port: int = DEFAULT_FRONTEND_PORT
    cors_origins: str = ""

    def render_env(self) -> str:
        lines = [
            "# Generated by tools/install.py — re-run to regenerate.",
            "# Hand-edits are preserved on re-run as defaults.",
            "",
            f"APP_MODE={self.mode}",
            "",
            "# Postgres",
            "POSTGRES_USER=oj",
            f"POSTGRES_PASSWORD={self.db_password}",
            "POSTGRES_DB=codefyui_oj",
            "",
            "# JWT (rotate for production deployments)",
            f"JWT_SECRET={self.jwt_secret}",
            "",
            f"FRONTEND_PORT={self.frontend_port}",
            f"CORS_ORIGINS={self.cors_origins}",
            "",
            "# Judge: subprocess (false) is fine for dev. true requires the",
            "# sandbox image built via backend/docker/build_sandbox.{ps1,sh}.",
            "JUDGE_USE_DOCKER=false",
        ]
        if self.mode == "competition" and self.bootstrap_admin:
            lines += [
                "",
                "# One-shot admin bootstrap. Removed on first successful boot",
                "# (the resulting user persists in the DB).",
                f"BOOTSTRAP_ADMIN_EMAIL={self.admin_email}",
                f"BOOTSTRAP_ADMIN_PASSWORD={self.admin_password}",
                f"BOOTSTRAP_ADMIN_DISPLAY_NAME={self.admin_display_name}",
            ]
        return "\n".join(lines) + "\n"

    def default_cors_origins(self) -> str:
        port = self.frontend_port
        origins = [f"http://localhost:{port}", f"http://127.0.0.1:{port}"]
        return '[' + ','.join(f'"{o}"' for o in origins) + ']'


# ---------------------------------------------------------------------------
# Existing .env parsing (for re-run defaults)
# ---------------------------------------------------------------------------


_ENV_LINE = re.compile(r"^\s*([A-Z][A-Z0-9_]*)\s*=\s*(.*)$")


def parse_existing_env(path: Path) -> dict[str, str]:
    if not path.exists():
        return {}
    out: dict[str, str] = {}
    for line in path.read_text(encoding="utf-8").splitlines():
        if not line or line.lstrip().startswith("#"):
            continue
        m = _ENV_LINE.match(line)
        if not m:
            continue
        key, val = m.group(1), m.group(2).strip()
        # Strip surrounding quotes the way docker compose does.
        if len(val) >= 2 and val[0] == val[-1] and val[0] in ('"', "'"):
            val = val[1:-1]
        out[key] = val
    return out


def settings_from_existing(env: dict[str, str]) -> Settings:
    s = Settings()
    s.mode = env.get("APP_MODE", s.mode)
    if s.mode not in ("competition", "practice"):
        s.mode = "competition"
    s.db_password = env.get("POSTGRES_PASSWORD") or s.db_password
    s.jwt_secret = env.get("JWT_SECRET") or s.jwt_secret
    try:
        s.frontend_port = int(env.get("FRONTEND_PORT") or s.frontend_port)
    except ValueError:
        pass
    s.cors_origins = env.get("CORS_ORIGINS") or s.default_cors_origins()
    s.admin_email = env.get("BOOTSTRAP_ADMIN_EMAIL", "")
    s.admin_password = env.get("BOOTSTRAP_ADMIN_PASSWORD", "")
    s.admin_display_name = env.get("BOOTSTRAP_ADMIN_DISPLAY_NAME", "Admin")
    s.bootstrap_admin = bool(s.admin_email and s.admin_password)
    return s


