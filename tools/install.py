def _run(cmd: list[str]) -> tuple[bool, str]:
    try:
        out = subprocess.run(cmd, capture_output=True, text=True, check=False, timeout=10)
        if out.returncode != 0:
            return False, (out.stderr or out.stdout or "").strip()
        return True, (out.stdout or "").strip().splitlines()[0] if out.stdout else "ok"
    except (FileNotFoundError, subprocess.TimeoutExpired) as e:
        return False, str(e)


def _port_free(port: int) -> bool:
    # Docker on Windows publishes ports via a userland proxy that doesn't
    # always collide with bind() on 0.0.0.0. The reliable check is "can
    # something accept a connection on localhost:port" — if it accepts or
    # times out (firewalled / loaded), assume in use. Only an explicit
    # ConnectionRefusedError means the port is truly idle.
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.settimeout(1.0)
        try:
            s.connect(("127.0.0.1", port))
        except ConnectionRefusedError:
            return True
        except OSError:
            return False
        return False


def env_checks(ports_to_check: list[int]) -> list[tuple[str, bool, str]]:
    results: list[tuple[str, bool, str]] = []

    if shutil.which("docker") is None:
        results.append(("Docker installed", False, "docker not in PATH"))
    else:
        ok, msg = _run(["docker", "--version"])
        results.append(("Docker installed", ok, msg))

    ok, msg = _run(["docker", "compose", "version"])
    results.append(("docker compose plugin", ok, msg))

    ok, msg = _run(["docker", "info"])
    if ok:
        results.append(("Docker daemon running", True, "ok"))
    else:
        first = msg.splitlines()[0] if msg else "daemon unreachable"
        results.append(("Docker daemon running", False, first))

    for port in ports_to_check:
        free = _port_free(port)
        results.append((f"Port {port} available", free, "free" if free else "already in use"))

    return results


def render_check_table(results: list[tuple[str, bool, str]]) -> Table:
    table = Table(show_header=True, header_style="bold")
    table.add_column("Check")
    table.add_column("Status")
    table.add_column("Detail", overflow="fold")
    for name, ok, detail in results:
        status = "[green]OK[/green]" if ok else "[red]FAIL[/red]"
        table.add_row(name, status, detail)
    return table


